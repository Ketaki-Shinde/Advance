using System;


namespace Class_Training_Day1
{
    class DoWhileLoop
    {
        public static void Main1()
        {
            //do-while
            //syntax
            //do //We do a task and then check the condition
            //{
            //task
            //}
            //while(condition)
            // WAP to print your name 5 times
            int counter = 0;
